//-----------------------------------------------------------------------------
// Texture.cpp
//-----------------------------------------------------------------------------
#include <SDL.h>

#include "Texture.h"

//-----------------------------------------------------------------------------
Texture::Texture(SDL_Texture* texture, int w, int h) : texture_(texture)                     
{
  size_ = { w, h };
}

//-----------------------------------------------------------------------------
Texture::~Texture()
{
  SDL_DestroyTexture(texture_);
}

//-----------------------------------------------------------------------------
SDL_Texture* Texture::getSDLTexture()
{
  return texture_;
}

//-----------------------------------------------------------------------------
std::pair<int, int>* Texture::getSize()
{
  return &size_;
}